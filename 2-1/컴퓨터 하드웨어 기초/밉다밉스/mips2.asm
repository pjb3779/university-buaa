.data
array: .space 400          # 100개의 워드를 위한 400바이트 공간 할당

.text
.globl main

# main 함수
main:
    li $a0, 10            # $a0 <- 10 (첫 10개의 피보나치 수를 계산)
    la $a1, array         # $a1에 배열의 시작 주소 로드
    jal FIB               # FIB 함수 호출

    # 프로그램 종료
    li $v0, 10            # 시스템 호출 코드 10 (프로그램 종료)
    syscall
    
# FIB 함수: $a0에 N, $a1에 배열의 시작 주소가 전달됨
FIB:
    li $t0, 1             # $t0 <- 1 (첫 번째 피보나치 수)
    li $t1, 1             # $t1 <- 1 (두 번째 피보나치 수)
    sw $t0, 0($a1)        # 배열의 첫 번째 요소에 1 저장
    addi $a1, $a1, 4      # 배열 포인터 증가

    # 첫 번째 항 출력
    move $a0, $t0         # $a0 <- $t0 (출력할 값)
    li $v0, 1             # 시스템 호출 코드 1 (정수 출력)
    syscall               # 첫 번째 항 출력

    # 줄 바꿈
    li $v0, 11            # 시스템 호출 코드 11 (문자 출력)
    li $a0, 10            # 줄 바꿈 문자 ('\n')
    syscall               # 줄 바꿈 출력

    sw $t1, 0($a1)        # 배열의 두 번째 요소에 1 저장
    addi $a1, $a1, 4      # 배열 포인터 증가

    # 두 번째 항 출력
    move $a0, $t1         # $a0 <- $t1 (출력할 값)
    li $v0, 1             # 시스템 호출 코드 1 (정수 출력)
    syscall               # 두 번째 항 출력

    # 줄 바꿈
    li $v0, 11            # 시스템 호출 코드 11 (문자 출력)
    li $a0, 10            # 줄 바꿈 문자 ('\n')
    syscall               # 줄 바꿈 출력

    subi $a0, $a0, 2      # $a0 <- $a0 - 2 (첫 두 개의 요소를 이미 저장했으므로 N-2)

FIB_loop:
    blez $a0, FIB_end     # $a0 <= 0이면 루프 종료

    add $t2, $t0, $t1     # $t2 <- $t0 + $t1 (다음 피보나치 수 계산)
    sw $t2, 0($a1)        # 배열의 현재 위치에 $t2 저장
    addi $a1, $a1, 4      # 배열 포인터 증가

    # 현재 항 출력
    move $a0, $t2         # $a0 <- $t2 (출력할 값)
    li $v0, 1             # 시스템 호출 코드 1 (정수 출력)
    syscall               # 현재 피보나치 수 출력

    # 줄 바꿈
    li $v0, 11            # 시스템 호출 코드 11 (문자 출력)
    li $a0, 10            # 줄 바꿈 문자 ('\n')
    syscall               # 줄 바꿈 출력

    move $t0, $t1         # $t0 <- $t1 (이전 값 업데이트)
    move $t1, $t2         # $t1 <- $t2 (이전 값 업데이트)

    subi $a0, $a0, 1      # $a0 <- $a0 - 1 (남은 횟수 감소)
    j FIB_loop            # FIB_loop로 분기

FIB_end:
    jr $ra                # 호출한 곳으로 복귀

